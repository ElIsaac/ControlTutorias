    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String materia=JOptionPane.showInputDialog(null,"Ingrese el nombre de la materia");
        int aux=JOptionPane.showConfirmDialog(null, "Â¿Aprobo?");
        if(aux==JOptionPane.OK_OPTION){
            Connection conn;
            try {
                conn = (Connection) DriverManager.getConnection(url, user, pass);
                PreparedStatement pps = conn.prepareStatement("insert into materias (ID,noDeLista,materia,aprobada) VALUES (?,?,?,?)");
            pps.setString(1, "0");
            pps.setString(2, txtnoDeLista.getText().trim());
            pps.setString(3, materia);
            pps.setString(4, "si");
            pps.executeUpdate();
            } catch(Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
             
        }
        else if(aux==JOptionPane.NO_OPTION){
              Connection conn;
            try {
                conn = (Connection) DriverManager.getConnection(url, user, pass);
                PreparedStatement pps = conn.prepareStatement("insert into materias (ID,noDeLista,materia,aprobada) VALUES (?,?,?,?)");
            pps.setString(1, "0");
            pps.setString(2, txtnoDeLista.getText().trim());
            pps.setString(3, materia);
            pps.setString(4, "no");
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos guardados exitosamente");
            txtnoDeLista.setText("");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "salio del registro");
        }
            
    }//GEN-LAST:event_jButton2ActionPerformed

